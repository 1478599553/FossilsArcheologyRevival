    protected void chestWithLoot(int i1, int j1, int k1, boolean isTrapped, World world, Random rand, WeightedRandomChestContent[] content, int i) {
        if (isTrapped) {
            world.setBlockState(i1, j1, k1, Blocks.TRAPPED_CHEST, 0, 2);
        } else {
            world.setBlockState(i1, j1, k1, Blocks.CHEST, 0, 2);
        }
        TileEntityChest tile = (TileEntityChest) world.getTileEntity(i1, j1, k1);
        if (tile != null) {
            WeightedRandomChestContent.generateChestContents(rand, content, tile, i);
        }
    }

